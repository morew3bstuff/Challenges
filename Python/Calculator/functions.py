# Addition Function
def add(n1, n2):
    return n1 + n2

# Substraction Function
def subtract(n1, n2):
    return n1 - n2

# Multiplication Function
def multiply(n1, n2):
    return n1 * n2

# Division Function
def divide(n1, n2):
    return n1 / n2

operations = {
    "+" : add,
    "-" : subtract,
    "*" : multiply,
    "/" : divide
}

